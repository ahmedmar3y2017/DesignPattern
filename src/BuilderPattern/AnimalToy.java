package BuilderPattern;

public class AnimalToy {

  String AnimalName;
  String AnimalHead;
  String AnimalBody;
  String AnimalLeg;
  String AnimalArm;
  String AnimalTail;

  public String getAnimalName() {
    return AnimalName;
  }

  public void setAnimalName(String animalName) {
    AnimalName = animalName;
  }

  public String getAnimalHead() {
    return AnimalHead;
  }

  public void setAnimalHead(String animalHead) {
    AnimalHead = animalHead;
  }

  public String getAnimalBody() {
    return AnimalBody;
  }

  public void setAnimalBody(String animalBody) {
    AnimalBody = animalBody;
  }

  public String getAnimalLeg() {
    return AnimalLeg;
  }

  public void setAnimalLeg(String animalLeg) {
    AnimalLeg = animalLeg;
  }

  public String getAnimalArm() {
    return AnimalArm;
  }

  public void setAnimalArm(String animalArm) {
    AnimalArm = animalArm;
  }

  public String getAnimalTail() {
    return AnimalTail;
  }

  public void setAnimalTail(String animalTail) {
    AnimalTail = animalTail;
  }

}
